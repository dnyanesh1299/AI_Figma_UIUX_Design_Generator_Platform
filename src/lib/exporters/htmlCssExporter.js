/** Derive a gradient color pair from a label/keyword for product image slots */
function _productGradient(label) {
  const l = (label || '').toLowerCase();
  if (/gold|golden|luxury/.test(l))    return ['#b8860b', '#d4af37'];
  if (/diamond|crystal|gem/.test(l))   return ['#a8d8f0', '#6cb4e4'];
  if (/silver|steel|metal/.test(l))    return ['#708090', '#a9b9c0'];
  if (/rose|pink|blush/.test(l))       return ['#c4627a', '#e8a0b0'];
  if (/ruby|red|garnet/.test(l))       return ['#9b1b30', '#d9534f'];
  if (/emerald|green|jade/.test(l))    return ['#046307', '#3cb371'];
  if (/sapphire|blue|aqua/.test(l))    return ['#0f3460', '#16213e'];
  if (/pearl|white|ivory/.test(l))     return ['#f8f8ff', '#d3d3d3'];
  if (/new|sale|hot/.test(l))          return ['#7c3aed', '#a855f7'];
  return ['var(--color-primary)', 'var(--color-secondary)'];
}

/** Pick an emoji icon based on product category/badge keyword */
function _productIcon(label) {
  const l = (label || '').toLowerCase();
  if (/ring/.test(l))       return '💍';
  if (/necklace|chain/.test(l)) return '📿';
  if (/bracelet|bangle/.test(l)) return '📿';
  if (/earring/.test(l))    return '✨';
  if (/watch/.test(l))      return '⌚';
  if (/diamond/.test(l))    return '💎';
  if (/gold/.test(l))       return '🏅';
  if (/silver/.test(l))     return '🥈';
  if (/bag|purse/.test(l))  return '👜';
  if (/shoe|boot/.test(l))  return '👠';
  if (/shirt|top|cloth/.test(l)) return '👗';
  if (/phone|tech/.test(l)) return '📱';
  if (/book/.test(l))       return '📚';
  return '🛍️';
}

