// ─── Image Component ───────────────────────────────────────────────────────

function renderImage(comp) {
  const prompt = comp.properties?.imagePrompt || comp.properties?.title || comp.properties?.content || 'illustration';
  const aspectRatio = comp.properties?.aspectRatio || '16:9';

  let paddingBottom = '56.25%'; // default 16:9
  if (aspectRatio === '1:1') paddingBottom = '100%';
  else if (aspectRatio === '4:3') paddingBottom = '75%';
  else if (aspectRatio === '21:9') paddingBottom = '42.85%';
  else if (aspectRatio === '3:2') paddingBottom = '66.67%';

  const rawPrompt = comp.properties?.imagePrompt || comp.properties?.title || comp.properties?.content || 'mockup';
  const imgUrl = getPollinationsImageUrl(rawPrompt, 800, 450, getStableSeed(rawPrompt));

  return `
    <div id="${esc(comp.id || 'image')}" class="image-card" style="width:100%;padding-bottom:${paddingBottom};position:relative;border-radius:var(--radius-lg);overflow:hidden;border:1px solid var(--color-border);background-color:rgba(255,255,255,0.02);">
      <img src="${esc(imgUrl)}" alt="${esc(prompt)}" style="position:absolute;top:0;left:0;width:100%;height:100%;object-fit:cover;object-position:center;" />
    </div>`;
}

// ─── Main component dispatcher ─────────────────────────────────────────────

/**
 * @param {object} comp   - Component object from the schema
 * @param {object} [schema] - Full design schema (used by footer for navigation links)
 */
function renderComponent(comp, schema) {
  switch (comp.type) {
    case 'navbar':           return renderNavbar(comp, schema);
    case 'hero':             return renderHero(comp);
    case 'metric-card':      return renderMetricCard(comp);
    case 'chart':            return renderChart(comp);
    case 'table':
    case 'data-table':       return renderDataTable(comp);
    case 'form':             return renderForm(comp);
    case 'button':           return renderButton(comp);
    case 'footer':           return renderFooter(comp, schema);
    case 'testimonial':      return renderTestimonial(comp);
    case 'pricing-card':     return renderPricingCard(comp);
    case 'banner':           return renderBanner(comp);
    case 'list':             return renderList(comp);
    case 'stats-bar':        return renderStatsBar(comp);
    case 'product-card':     return renderProductCard(comp);
    case 'collection-card':  return renderCollectionCard(comp);
    case 'sidebar':          return renderSidebar(comp, schema);
    case 'card':             return renderCard(comp);
    case 'image':            return renderImage(comp);
    default:
      return `
    <div class="card" id="${esc(comp.id || comp.type)}" style="opacity:0.7">
      <p class="metric-label">${esc(comp.type)}</p>
      <h3>${esc(comp.label || comp.properties?.title || comp.type)}</h3>
    </div>`;
  }
}

// ─── Page HTML builder ─────────────────────────────────────────────────────

function buildPageHtml(page, schema, projectName, isIndex = false) {
  const colors = schema?.tokens?.colors || {};
  const typo = schema?.tokens?.typography || {};
  const headingFont = typo.headingFont || 'Outfit';
  const bodyFont = typo.bodyFont || 'Inter';
  const bgColor = page.layout?.background || colors.background || '#0f172a';
  const pageName = esc(page.name || 'Page');
  const title = `${esc(projectName)} — ${pageName}`;
  const cssPath = isIndex ? 'styles.css' : '../styles.css';

  const hasSidebar = page.components?.some(c => c.type === 'sidebar');
  const sidebarComp = page.components?.find(c => c.type === 'sidebar');
  const otherComps = page.components?.filter(c => c.type !== 'sidebar') || [];

  // Detect special full-width components
  const fullWidthTypes = new Set(['navbar', 'footer', 'banner', 'hero', 'stats-bar']);

  // Build body content
  let bodyContent = '';

  if (hasSidebar) {
    // Layout with sidebar
    const sidebarHtml = renderComponent(sidebarComp, schema);
    const contentComps = otherComps.filter(c => c.type !== 'navbar' && c.type !== 'footer');
    const navbarComp = otherComps.find(c => c.type === 'navbar');
    const footerComp = otherComps.find(c => c.type === 'footer');

    if (navbarComp) bodyContent += renderComponent(navbarComp, schema);

    bodyContent += `\n  <div class="layout-with-sidebar">`;
    bodyContent += sidebarHtml;
    bodyContent += `\n    <main class="sidebar-content">`;

    // Grid layout for content components
    const gridComps = contentComps.filter(c => !fullWidthTypes.has(c.type));
    if (gridComps.length > 0) {
      bodyContent += `\n      <div class="grid-12" style="margin-bottom:1.5rem;">`;
      gridComps.forEach(comp => {
        const pos = comp?.responsive?.desktop?.position || comp?.position || {};
        const span = Math.max(1, Math.min(12, pos.w ?? 12));
        bodyContent += `\n        <div class="col-span-${span}">${renderComponent(comp, schema)}\n        </div>`;
      });
      bodyContent += `\n      </div>`;
    }

    bodyContent += `\n    </main>\n  </div>`;
    if (footerComp) bodyContent += renderComponent(footerComp, schema);
  } else {
    // Re-process with grid grouping
    let i = 0;
    const comps = otherComps;
    while (i < comps.length) {
      const comp = comps[i];
      if (fullWidthTypes.has(comp.type)) {
        bodyContent += renderComponent(comp, schema);
        i++;
      } else {
        // Gather consecutive non-fullwidth components into a shared grid row
        bodyContent += `\n  <div class="page-wrapper"><div class="grid-12">`;
        while (i < comps.length && !fullWidthTypes.has(comps[i].type)) {
          const c = comps[i];
          const pos = c?.responsive?.desktop?.position || c?.position || {};
          const span = Math.max(1, Math.min(12, pos.w ?? 6));
          bodyContent += `\n    <div class="col-span-${span}">${renderComponent(c, schema)}\n    </div>`;
          i++;
        }
        bodyContent += `\n  </div></div>`;
      }
    }
  }

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <meta name="description" content="${esc(projectName)} — ${pageName}">
  <title>${title}</title>
  <link rel="preconnect" href="https://fonts.googleapis.com">
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
  <link rel="stylesheet" href="${cssPath}">
  <link rel="stylesheet" href="https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.7.2/css/all.min.css" crossorigin="anonymous">
  <style>
    /* Page-specific overrides */
    body { background-color: ${bgColor}; }
  </style>
</head>
<body>
${bodyContent}
</body>
</html>`;
}

// ─── Main Export Function ───────────────────────────────────────────────────

/**
 * Generates an array of { filename, content } file objects from the schema.
 * @param {object} schema
 * @param {object} options - { projectName }
 * @returns {Promise<{ type: 'files', files: Array<{filename, content, mimeType}> }>}
 */
export async function generateHtmlCssExport(schema, options = {}) {
  const projectName =
    options.projectName ||
    schema?.meta?.projectName ||
    'design-export';

  const pages = schema?.pages || [];
  const cssContent = generateCSS(schema);
  const files = [];

  // styles.css (shared)
  files.push({ filename: 'styles.css', content: cssContent, mimeType: 'text/css' });

  // index.html → first page
  if (pages.length > 0) {
    files.push({
      filename: 'index.html',
      content: buildPageHtml(pages[0], schema, projectName, true),
      mimeType: 'text/html',
    });
  }

  // Additional pages
  pages.slice(1).forEach(page => {
    const pageName = slugify(page.name || page.id || `page-${pages.indexOf(page)}`);
    files.push({
      filename: `${pageName}.html`,
      content: buildPageHtml(page, schema, projectName, false),
      mimeType: 'text/html',
    });
  });

  // README
  files.push({
    filename: 'README.md',
    content: generateReadme(schema, projectName, files),
    mimeType: 'text/markdown',
  });

  return { type: 'files', files };
}

