// ─── Helper: Pollinations AI Image Generation ─────────────────────────────

function getStableSeed(str) {
  if (!str) return 42;
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  return Math.abs(hash) % 1000000;
}

function getPollinationsImageUrl(prompt, width = 600, height = 400, seed = null) {
  const cleanPrompt = encodeURIComponent(String(prompt || "mockup").trim());
  const publishableKey = import.meta.env.VITE_POLLINATIONS_API_KEY;
  let url = `https://image.pollinations.ai/prompt/${cleanPrompt}?width=${width}&height=${height}&nologo=true&private=true`;
  if (publishableKey) {
    url += `&key=${publishableKey}`;
  }
  if (seed !== null) {
    url += `&seed=${seed}`;
  }
  return url;
}

// ─── Helper: hex → "r,g,b" for rgba() ─────────────────────────────────────

function hexToRgb(hex) {
  const h = hex.replace('#', '');
  if (h.length === 3) {
    const r = parseInt(h[0] + h[0], 16);
    const g = parseInt(h[1] + h[1], 16);
    const b = parseInt(h[2] + h[2], 16);
    return `${r},${g},${b}`;
  }
  const r = parseInt(h.slice(0, 2), 16);
  const g = parseInt(h.slice(2, 4), 16);
  const b = parseInt(h.slice(4, 6), 16);
  if (isNaN(r)) return '124,58,237';
  return `${r},${g},${b}`;
}

// ─── Helper: render gradient image placeholder ──────────────────────────────

/**
 * Renders a styled gradient <div> in place of an <img> tag.
 * Matches the pipeline spec: background gradient + centered icon/label.
 * @param {string} color1 - CSS color for gradient start
 * @param {string} color2 - CSS color for gradient end
 * @param {string} icon   - Emoji or text icon to display
 * @param {string} [label] - Optional short label below the icon
 */
function renderImgPlaceholder(color1, color2, icon, label) {
  const style = `background:linear-gradient(135deg,${color1},${color2})`;
  return `<div class="img-placeholder" style="${style}" aria-hidden="true">
    <span style="font-size:2rem">${icon}</span>
    ${label ? `<span>${esc(label)}</span>` : ''}
  </div>`;
}

// ─── Helper: render star rating glyphs ────────────────────────────────────

/**
 * Renders filled and empty star glyphs from a numeric rating.
 * @param {number} rating - 0–5 (decimals rounded to nearest 0.5)
 * @param {number} [max=5]
 */
function renderStars(rating, max = 5) {
  const filled = Math.round(Math.min(max, Math.max(0, Number(rating) || 5)));
  const stars = [];
  for (let i = 1; i <= max; i++) {
    stars.push(i <= filled ? '★' : '<span class="star-empty">★</span>');
  }
  return `<span class="star-rating" aria-label="${filled} out of ${max} stars">${stars.join('')}</span>`;
}

